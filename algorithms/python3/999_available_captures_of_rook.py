class Solution(object):
    def numRookCaptures(self, board):
        """
        :type board: List[List[str]]
        :rtype: int
        """
        def get_rook():
            for i in range(8):
                for j in range(8):
                    if board[i][j]=='R':
                        return [i,j]
        i,j=get_rook()
        res=0
        for k in range(j,-1,-1):
            if board[i][k]=='B':
                break
            elif board[i][k]=='p':
                res+=1
                break

        for k in range(j+1,8):
            if board[i][k]=='B':
                break
            elif board[i][k]=='p':
                res+=1
                break
        for k in range(i, -1, -1):
            if board[k][j] == 'B':
                break
            elif board[k][j] == 'p':
                res += 1
                break

        for k in range(i+1, 8):
            if board[k][j] == 'B':
                break
            elif board[k][j] == 'p':
                res += 1
                break
        return res

